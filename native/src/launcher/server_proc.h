#pragma once

/* ================================================================
 * server_proc.h — 本地 C 服务器子进程管理声明
 * ----------------------------------------------------------------
 * 管理本地翻译服务器（dst_server.exe）的生命周期：启动、停止、
 * 健康检查以及 UI 状态同步。
 * ================================================================ */

#include "globals.h"

/* 检查服务器是否存活（进程存在或 HTTP /health 返回 200） */
int server_alive(void);

/* 启动本地 C 服务器子进程；如果已运行则复用 */
int start_server(void);

/* 通过 /shutdown 端点优雅停止服务器，超时则强制终止 */
void stop_server(void);

/* 根据当前状态切换服务器运行/停止 */
void toggle_server(void);
